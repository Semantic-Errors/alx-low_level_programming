#include "3-calc.h"

/**
 * op_add - function that adds 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the sum of the numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - function that subtracts 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the subtraction of the numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - function that multiplies 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the multiplication of the numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - function that divides 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the division of the numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - function that calculates modulo of 2 numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the modulo of the numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
